function normalizeKey(input) {
    return String(input)
        .trim()
        .replace(/[\s\-]+/g, '_')
        .replace(/_+/g, '_')
        .toLowerCase()
}

const normalizedLabelMaps = (() => {
    const result = Object.create(null)
    for (const [key, value] of Object.entries(allLabelMaps)) {
        result[normalizeKey(key)] = value
    }
    return result
})()

export function translateLabel(label) {
    if (label == null) return ''
    const raw = String(label).trim()
    if (!raw) return ''

    // 1) exact match
    if (allLabelMaps[raw]) return allLabelMaps[raw]

    // 2) normalized match (handles spaces/underscores/case)
    const normalized = normalizeKey(raw)
    if (normalizedLabelMaps[normalized]) return normalizedLabelMaps[normalized]

    // 3) try swapping space/underscore explicitly (common backend variations)
    const swapped = raw.includes(' ') ? raw.replace(/\s+/g, '_') : raw.replace(/_+/g, ' ')
    if (allLabelMaps[swapped]) return allLabelMaps[swapped]

    const swappedNormalized = normalizeKey(swapped)
    if (normalizedLabelMaps[swappedNormalized]) return normalizedLabelMaps[swappedNormalized]

    return raw
}

function containsCJK(text) {
    return /[\u4e00-\u9fff]/.test(String(text || ''))
}

function humanizeEnglishLabel(value) {
    const raw = value == null ? '' : String(value).trim()
    if (!raw) return ''
    const noUnderscore = raw.replace(/[_\-]+/g, ' ')
    const deCamel = noUnderscore.replace(/([a-z])([A-Z])/g, '$1 $2')
    return deCamel.replace(/\s+/g, ' ').trim()
}

function extractEnglishKey(rawLabel) {
    const raw = rawLabel == null ? '' : String(rawLabel).trim()
    if (!raw) return ''
    // Support both half-width '(' and full-width '（'
    const canonical = raw.split(/[（(]/)[0].trim()
    return canonical || raw
}

/**
 * Format a label into a consistent "English (中文)" string.
 *
 * Input can be:
 * - a backend prediction item: { label_en, label_zh, label }
 * - a raw label string
 */
export function formatLabelDisplay(itemOrLabel) {
    const isObj = itemOrLabel && typeof itemOrLabel === 'object'
    const labelEnRaw = isObj ? (itemOrLabel.label_en ?? itemOrLabel.label ?? '') : itemOrLabel

    const englishKey = extractEnglishKey(labelEnRaw)
    if (!englishKey) return ''
    const englishText = humanizeEnglishLabel(englishKey)

    const zhFromFrontend = translateLabel(englishKey)
    const frontendLooksTranslated = zhFromFrontend && containsCJK(zhFromFrontend) && zhFromFrontend !== englishKey

    // Always use the frontend label map for Chinese to keep results consistent.
    // If no Chinese mapping exists, fall back to showing only English.
    if (!frontendLooksTranslated) return englishText

    const chosenZh = String(zhFromFrontend).trim()
    return `${englishText} (${chosenZh})`
}
